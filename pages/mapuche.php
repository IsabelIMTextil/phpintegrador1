        <p class="card-text"> Los tejidos de la cultura Mapuche se realizan en telar vertical o "witral", que en lengua Mapuche quiere decir "estar de pie". GEneralmente se teje afuera de la casa, con el telar apoyado en la pared. Los diseños de la tetxtilería mapuche están relacionados con la cosmovisión de este pueblo, una de las culturas originarias de la Patagonia, que constituye una unidad política, lingüística, cultural e histórica. Antiguamente se tejía principalmente con lana de guanaco, que después de la conquista fue desplazada por la oveja. Hoy en día también se utilizan fibras de cabra e hilo industrial.</p>
